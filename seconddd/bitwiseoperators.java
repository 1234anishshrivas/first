package seconddd;

public class bitwiseoperators 
{
    public static void main(String[] arg)
    {
        int a = 4;
        int b = 5;

        //a|b = OR
        //A&B = AND
        //A^B = XOR
        //~A = COMPLEMENT

        System.out.println("a|b = " + ( a | b) );
        System.out.println("a&b = " + (a & b));
        System.out.println("a^b = " + ( a ^ b));
        System.out.println("~a = " + ( ~a));





    }
    
}
